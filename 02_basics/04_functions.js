// function expression or function declaration
function add(a, b) {
    return a + b;
}

console.log(add(5,6));

// function expression

let sub = function(a, b) {
    return a - b;
}

console.log(sub(20, 13));

// named function expression

let mul = function multiply(x, y) {
    return x * y;
}

console.log(mul(5, 6));

// arrow function

let div = (x, y) => {
    return x / y;
}

console.log(30/10);
 
